CREATE TABLE [dbo].[Student] (
    [StudentId]   INT           NOT NULL,
    [StudentName] NVARCHAR (50) NOT NULL,
    [Age]         INT           NOT NULL, 
    [City] NVARCHAR(50) NOT NULL, 
    [Email] NVARCHAR(50) NOT NULL, 
    [Username] NVARCHAR(50) NOT NULL, 
    [Password] NVARCHAR(50) NULL
);

